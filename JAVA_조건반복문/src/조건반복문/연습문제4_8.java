package 조건반복문;
	//2x+4y=10 방정식 의 모든 해를 구하시오 단 x와 y는 정수이고 각각의 범위는0<=x<=10, 0<=y<=10이다
public class 연습문제4_8 {
	public static void main (String [] args) {
		
		int x = 0;
		int y = 0;
		
		for(x=0; x<=10; x++) {
			for(y=0; y<=10; y++) {
				if(2*x + 4*y == 10) {
					System.out.printf("x : %d , y : %d%n", x, y);
				}
			}
		}
	}
}
